/**
 * Search providers for the lookup pipeline.
 *
 * Phase 0: take a raw input string, pull the broadest set of plausible
 * references from the web, and write each result row as a `signal` so the
 * synthesis layer (and the UI's progress view) can see them stream in.
 *
 * Three providers run concurrently for discovery-style ("query") lookups:
 *
 *  - Parallel.ai Search (POST /v1beta/search): web-wide research grade
 *    search with Google-operator support. We ask an LLM to plan 12-20
 *    diverse queries, chunk them into batches of 5, and fire multiple
 *    Parallel.ai calls in parallel.
 *
 *  - Unipile LinkedIn Search (POST /api/v1/linkedin/search): LinkedIn
 *    Classic people search via the user's connected LinkedIn account.
 *    Uses a smaller, LinkedIn-boolean-syntax keyword set generated by
 *    the same LLM planner. One call per keyword.
 *
 *  - Exa People Search (POST /search with category="people"): hybrid
 *    retrieval over Exa's 1B+ indexed public profiles. Takes natural
 *    language queries ("VP of Product at Microsoft") rather than
 *    operators or booleans, so it gets its own planner output.
 *
 * Results from all three providers are merged, deduped by public URL,
 * then written as signals. The Unipile and Exa branches are best-effort:
 * if their env vars are missing, or if a call fails, we log and fall
 * back to whatever other providers did succeed.
 *
 * Non-discovery lookups (name/linkedin/email) still hit only Parallel.ai
 * via `buildSearchPlan`.
 *
 * Docs: https://docs.parallel.ai, https://developer.unipile.com,
 * https://docs.exa.ai
 */
const PARALLEL_SEARCH_URL = "https://api.parallel.ai/v1beta/search";
const PLANNER_MODEL = process.env.OPENAI_MODEL ?? "gpt-4o-2024-08-06";
// How many planner-generated queries to pack into each Parallel.ai request.
// Small batches = more concurrent calls = broader fan-out, at the cost of
// more HTTP round-trips. 5 is a compromise that keeps latency under the
// 60s budget while still hitting ~3-4 concurrent requests for a 15-query plan.
const QUERIES_PER_REQUEST = 5;

// Unipile LinkedIn search. DSN (subdomain + port) is per-tenant and set
// at account creation, e.g. "api8.unipile.com:13851". The account id is
// the default Centinel LinkedIn account; overridable via env for tests.
const UNIPILE_LINKEDIN_SEARCH_PATH = "/api/v1/linkedin/search";
const UNIPILE_ACCOUNT_ID =
  process.env.UNIPILE_ACCOUNT_ID ?? "8KMvZvHSQa6owclhc24iSA";
// LinkedIn Classic caps people search at 50 results per request.
const UNIPILE_LINKEDIN_LIMIT = 50;

// Exa People Search. The "people" category replaced "linkedin" in Dec 2025
// and now searches Exa's 1B+ indexed profiles via hybrid retrieval (fine-
// tuned embeddings + Exa Search). Best consumed with natural-language
// queries rather than operators.
const EXA_SEARCH_URL = "https://api.exa.ai/search";
const EXA_PEOPLE_LIMIT = 25;

