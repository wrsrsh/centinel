// Normalized shape that all three providers map into before being written
// as signal rows. Keeps the insertion loop source-agnostic.
type SignalCandidate = {
  url: string;
  source: string;
  title?: string;
  snippet: string;
  publishedAt?: number;
  raw: unknown;
};

export const runSearch = internalAction({
  args: { lookupId: v.id("lookups") },
  handler: async (ctx, { lookupId }) => {
    const apiKey = process.env.PARALLEL_API_KEY;
    if (!apiKey) {
      await ctx.runMutation(internal.lookups.setStatus, {
        lookupId,
        status: "error",
        error: "PARALLEL_API_KEY not set in Convex environment",
      });
      return;
    }

    // Read the lookup row to get the original input. We do this with a
    // fresh runQuery so the action stays stateless.
    const lookup = await ctx.runQuery(internal.lookups._getLookup, {
      lookupId,
    });
    if (!lookup) return;

    await ctx.runMutation(internal.lookups.setStatus, {
      lookupId,
      status: "searching",
    });

    // Build an objective + a fan-out of search queries.
    //
    //  - For name/linkedin/email, one objective with ~4 hand-tuned queries
    //    is enough; we issue a single Parallel.ai call.
    //  - For "query" (discovery), the LLM planner emits three distinct
    //    query sets (web, LinkedIn boolean, Exa natural language) which
    //    we fan out to three providers concurrently:
    //      · Parallel.ai in chunked batches (web search)
    //      · Unipile LinkedIn search, one call per keyword string
    //      · Exa People Search, one call per natural-language query
    //    Results from all three streams are merged, deduped by URL, and
    //    written as signals.
    let results: SignalCandidate[];
    try {
      if (lookup.inputType === "query") {
        const openaiApiKey = process.env.OPENAI_API_KEY;
        if (!openaiApiKey) {
          await ctx.runMutation(internal.lookups.setStatus, {
            lookupId,
            status: "error",
            error: "OPENAI_API_KEY not set in Convex environment",
          });
          return;
        }

        const plan = await planDiscoverySearch(openaiApiKey, lookup.input);
        const batches = chunk(plan.queries, QUERIES_PER_REQUEST);

        const [parallelCandidates, unipileCandidates, exaCandidates] =
          await Promise.all([
            Promise.all(
              batches.map((batch) =>
                callParallelSearch(apiKey, plan.objective, batch),
              ),
            ).then((batched) => batched.flat().map(parallelToSignal)),
            runUnipileLinkedinFanOut(plan.linkedin_queries),
            runExaPeopleFanOut(plan.exa_queries),
          ]);

        results = dedupeSignalsByUrl([
          ...parallelCandidates,
          ...unipileCandidates,
          ...exaCandidates,
        ]);
      } else {
        const { objective, queries } = buildSearchPlan(
          lookup.inputType,
          lookup.input,
        );
        const raw = await callParallelSearch(apiKey, objective, queries);
        results = raw.map(parallelToSignal);
      }
    } catch (err) {
      await ctx.runMutation(internal.lookups.setStatus, {
        lookupId,
        status: "error",
        error: (err as Error).message,
      });
      return;
    }

    // Insert all signals concurrently. Per-signal try/catch via allSettled
    // so that one bad row (oversized raw payload, unexpected field, etc.)
    // doesn't kill the whole pipeline — we log the offender and move on.
    const inserts = await Promise.allSettled(
      results.map((s) =>
        ctx.runMutation(internal.lookups.insertSignal, {
          lookupId,
          source: s.source,
          sourceUrl: s.url || undefined,
          title: s.title || undefined,
          snippet: s.snippet || " ",
          publishedAt: s.publishedAt,
          raw: safeRawForConvex(s.raw),
        }),
      ),
    );
    for (let i = 0; i < inserts.length; i++) {
      const outcome = inserts[i];
      if (outcome.status === "rejected") {
        const s = results[i];
        console.error(
          `insertSignal failed for ${s.source} ${s.url}:`,
          outcome.reason,
          { titleLen: s.title?.length ?? 0, snippetLen: s.snippet.length },
        );
      }
    }

    await ctx.runMutation(internal.lookups.setStatus, {
      lookupId,
      status: "resolving",
    });

    // Hand off to the synthesis layer. For now this is a stub; the next
    // milestone wires in Claude with structured output.
    await ctx.scheduler.runAfter(0, internal.synth.synthesize, { lookupId });
  },
});

